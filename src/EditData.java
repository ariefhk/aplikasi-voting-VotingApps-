    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
         if (dataBaru) {
            try {
                String sql = "INSERT INTO tb_mahasiswa(id_mahasiswa,nama,kelas,jurusan,username,password) VALUES ('%s', '%s', '%s', '%s', '%s', '%s' )";
                
                sql = String.format(sql,
                        txtId.getText(),
                        txtNama.getText(),
                        txtKelas.getText(),
                        txtJurusan.getText(),
                        txtUsername.getText(),
                        txtPassword.getText()
                );
                Connection conn = koneksi.getConnect();
                PreparedStatement pat = conn.prepareStatement(sql);
                pat.execute();
                JOptionPane.showMessageDialog(null, "Berhasil Disimpan");
            } catch (SQLException ex) {
               JOptionPane.showMessageDialog(null, "Gagal Disimpan" + ex);
            }
            
        }else {
            try {
                String sql = "UPDATE tb_mahasiswa SET nama='%s', kelas='%s', jurusan='%s', username='%s', password='%s' WHERE id_mahasiswa='%s'";

                sql = String.format(sql,
                        txtNama.getText(),
                        txtKelas.getText(),
                        txtJurusan.getText(),
                        txtUsername.getText(),
                        txtPassword.getText(),
                        txtId.getText()
                );
                Connection conn = koneksi.getConnect();
                PreparedStatement pat = conn.prepareStatement(sql);
                pat.execute();
                JOptionPane.showMessageDialog(null, "Berhasil DiEdit");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Gagal DiEdit" + ex);
            }

        }
        getData();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        try {
            // TODO add your handling code here:
            dataBaru = false;
            int row = table.getSelectedRow();
            String nimMhs = table.getModel().getValueAt(row, 0).toString();
            Connection conn = koneksi.getConnect();
            Statement stm = conn.createStatement();
            ResultSet result = stm.executeQuery("SELECT * FROM tb_mahasiswa WHERE id_mahasiswa=' " +nimMhs+"'");
            
            if (result.next()) {
                txtId.setText(result.getString("id_mahasiswa"));
                txtNama.setText(result.getString("nama"));
                txtKelas.setText(result.getString("kelas"));
                txtJurusan.setText(result.getString("jurusan"));
                txtUsername.setText(result.getString("username"));
                txtPassword.setText(result.getString("password"));
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(EditData.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tableMouseClicked

