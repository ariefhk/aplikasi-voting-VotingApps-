    private void getData(){
        try {
            Connection conn = koneksi.getConnect();
            Statement stm = conn.createStatement();
            ResultSet result = stm.executeQuery("SELECT * FROM tb_mahasiswa");
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            
            model.setRowCount(0);
            while (result.next()) {
                String id = result.getString("id_mahasiswa");
                String nama = result.getString("nama");
                String kelas = result.getString("kelas");
                String jurusan = result.getString("jurusan");
                String username = result.getString("username");
                String password = result.getString("password");
                
                model.addRow(new Object[]{id, nama,kelas, jurusan, username,password});
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(EditData.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
